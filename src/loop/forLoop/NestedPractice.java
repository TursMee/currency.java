package loop.forLoop;

public class NestedPractice {
    public static void main(String[] args) {
        for(int i=0;i<3;i++){
            System.out.println("I am "+ i +" For Loop");
            for(int j=0;j<5;j++){
                System.out.println("I am Nested For Loop");
                for(int k=0;k<2;k++){
                    System.out.println("KKKK");
                }

            }


        }













    }
}
