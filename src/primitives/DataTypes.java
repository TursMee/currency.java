package primitives;

public class DataTypes {
    public static void main (String[] args){
        byte myAge = 25;
        System.out.println("hello my age is " +myAge);

        short anyNum = 32123;
        System.out.println(anyNum);
        int num3 = 567;
        System.out.println(num3);
        long number4 = 7263761763768L;
        long dali = 200_000_000_000L;
        System.out.println(dali);
        System.out.println(number4);
        float number5 = 1.9f;
        System.out.println(number5);
        double number6 = 9;
        System.out.println(number6);
        double number7 = 98.995;
        System.out.println(number7);



    }
}
